package OneNet3Cuboids;

public class CoordWithRotation {

	private int a;
	private int b;
	private int c;
	private int rot;
	
	public CoordWithRotation(int a, int b, int c, int rot) {
		super();
		this.a = a;
		this.b = b;
		this.c = c;
		this.rot = rot;
	}

	public int getA() {
		return a;
	}

	public int getB() {
		return b;
	}

	public int getC() {
		return c;
	}

	public int getRot() {
		return rot;
	}
	
	
}
